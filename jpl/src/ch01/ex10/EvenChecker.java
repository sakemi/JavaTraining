package ch01.ex10;

public class EvenChecker {
	private int num;
	private boolean isEven;

	public EvenChecker(int num) {
		this.num = num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public boolean isEven() {
		return isEven;
	}

	public void setEven(boolean isEven) {
		this.isEven = isEven;
	}
}
